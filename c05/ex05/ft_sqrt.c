int	ft_sqrt(int nb)
{
	int	i;

	i = 2;
	if (nb <= 0)
		return (0);
	else if (nb == 1)
		return (1);
	while (i <= nb / i)
	{
		if (nb % i == 0)
			if (i == nb / i)
				return (i);
		i++;
	}
	return (0);
}
