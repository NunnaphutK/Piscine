#include <unistd.h>
#include "ft_stock_str.h"

void	ft_putstr(char *str)
{
	while (*str != '\0')
	{
		write(1, str, 1);
		str++;
	}
}

void	ft_putnbr(int nb)
{
	char	x;

	if (nb < 0 && nb > -10)
		write(1, "-", 1);
	else if (nb > 9 || nb < -9)
	{
		ft_putnbr(nb / 10);
	}
	nb = nb % 10;
	if (nb < 0)
		nb = -nb;
	x = nb + 48;
	write(1, &x, 1);
}

void	ft_show_tab(struct s_stock_str *par)
{
	int	i;

	i = 0;
	while (par[i].str != 0)
	{
		ft_putstr(par[i].str);
		ft_putstr("\n");
		ft_putnbr(par[i].size);
		ft_putstr("\n");
		ft_putstr(par[i].copy);
		ft_putstr("\n");
		i++;
	}
}
